// +build go1.8

package http

import (
	"io"
	"net/http"
)

// StatusCodeTracker net/http status code tracker
type StatusCodeTracker struct {
	http.ResponseWriter
	Status int
}

// WriteHeader ...
func (w *StatusCodeTracker) WriteHeader(status int) {
	w.Status = status
	w.ResponseWriter.WriteHeader(status)
}

